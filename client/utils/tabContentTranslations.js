// Helper function to get translated content
export const getTabTranslation = (category, key, language = 'en') => {
  try {
    const translation = tabContentTranslations[category]?.[key]?.[language]
    return translation || tabContentTranslations[category]?.[key]?.['en'] || key
  } catch (error) {
    console.warn(`Tab translation not found for ${category}.${key}.${language}`)
    return key
  }
}